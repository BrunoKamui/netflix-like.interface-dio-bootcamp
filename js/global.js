function openAnime(anime) {
	const title = document.querySelector('.title');
	title.innerHTML = anime.name;

	const description = document.querySelector('.description');
	description.innerHTML = anime.description;

	const image = document.querySelector('.main-title');
	image.style.backgroundImage = anime.image;

	const btnWatch = document.querySelector('#btn-watch');
	btnWatch.setAttribute('onclick', `watch(${anime.id})`);

	const btnInfo = document.querySelector('#btn-info');
	btnInfo.setAttribute('onclick', `info(${anime.id})`);
}

function watch(anime) {
	const modal = document.querySelector('.modal-wrapper');
	modal.setAttribute('class', 'modal-wrapper');

	const iframe = document.querySelector('.iframe');
	iframe.setAttribute('src', `${anime.link}`);
}

function info(anime) {
	const modalInfo = document.querySelector('.modal-wrapper-info');
	modalInfo.setAttribute('class', 'modal-wrapper-info');

	const info = document.querySelector('.modal-info');
	info.innerHTML = `<strong>SINOPSE:</strong> <br> ${anime.info}`;
}

function closeModal() {
	const modal = document.querySelector('.modal-wrapper');
	modal.setAttribute('class', 'modal-wrapper hidden');

	const modalInfo = document.querySelector('.modal-wrapper-info');
	modalInfo.setAttribute('class', 'modal-wrapper-info hidden');

	const info = document.querySelector('.modal-info');
	info.innerHTML = '';

	const iframe = document.querySelector('.iframe');
	iframe.setAttribute('src', '')
}


// load menu mobile

window.onload = function () {
	const menuMobile = document.querySelector('#menu-mobile');
	const menu = document.querySelector('nav');
	menuMobile.addEventListener('click', function () {
		if(menu.style.display == 'none') {
			menu.style.display = 'flex';
		}
		else {
			menu.style.display = 'none';
		}
	});
}
